public enum IdType {
    /**
     * 数据库ID自增
     */
    AUTO(0),
    /**
     * 该类型为未设置主键类型
     */
    NONE(1),
    /**
     * 用户输入ID
     * 该类型可以通过自己注册自动填充插件进行填充
     */
    INPUT(2),

    /**
     * 全局唯一ID (UUID)
     */
    UUID(3);

    private int key;

    IdType(int key) {
        this.key = key;
    }
}
